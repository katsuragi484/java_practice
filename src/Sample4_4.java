public class Sample4_4 {
    public static void main(String[] args){
        int num1 = 10;
        int num2 = 5;

        System.out.println("num1とnum2に色々な演算を行います。");
        System.out.println("num1+num2は" + (num1+num2) +"です。");
        System.out.println("num1-num2は" + (num1-num2) +"です。");
        System.out.println("num1*num2は" + (num1*num2) +"です。");
        System.out.println("num1/num2は" + (num1/num2) +"です。");
        System.out.println("num1%num2は" + (num1%num2) +"です。");
    }
}
